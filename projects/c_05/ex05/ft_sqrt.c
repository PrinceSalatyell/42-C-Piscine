int	ft_sqrt(int nb)
{
	int	i;

	i = 0;
	if (nb < 1)
		return (0);
	while ((i * i) <= nb && i < 46341)
		if (i * i == nb)
			break ;
	else
			i++;
	if (i * i == nb)
		return (i);
	else
		return (0);
}
