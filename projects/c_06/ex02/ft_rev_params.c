#include <unistd.h>

int	main(int n, char **args)
{
	int	i;
	int	j;

	if (n == 0)
		return (0);
	i = 0;
	j = n - 1;
	while (j > 0)
	{
		while (args[j][i])
		{
			write(1, &args[j][i], 1);
			i++;
		}
		write(1, "\n", 1);
		i = 0;
		j--;
	}
	return (0);
}
