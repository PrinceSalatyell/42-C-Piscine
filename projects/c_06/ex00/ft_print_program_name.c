#include <unistd.h>

int	main(int n, char **args)
{
	int	i;

	if (n == 0)
		return (0);
	i = 0;
	while (args[0][i])
		write(1, &args[0][i++], 1);
	write(1, "\n", 1);
	return (0);
}
