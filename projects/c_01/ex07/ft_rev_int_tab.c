#include<stdio.h>

void	ft_rev_int_tab(int *tab, int size)
{
	int	x;
	int	i;

	i = 0;
	x = 0;
	size--;
	while (i < size)
	{
		x = tab[i];
		tab[i] = tab[size];
		tab[size] = x;
		i++;
		size--;
	}
}
